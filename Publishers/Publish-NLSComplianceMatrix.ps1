#Requires -Version 7.0
#
# Publish-NLSComplianceMatrix.ps1  (v4.5.5)
# Generates a professional XLSX compliance matrix from assessment findings.
#
# Sheets:
#   1. Summary        — Overall posture, score, workload breakdown, framework scores
#   2. All Controls   — Every control with status, severity, framework citations
#   3. Gaps Only      — Filtered to Gap/Partial, with remediation commands
#   4. CIS M365 v6    — Controls mapped to CIS sections
#   5. CISA SCuBA     — Controls mapped to SCuBA policy IDs
#   6. NIST 800-53    — Controls mapped to NIST control families
#   7. CMMC 2.0       — Controls mapped to CMMC practices
#   8. ISO 27001      — Controls mapped to ISO 27001:2022 clauses
#   9. SOC 2          — Controls mapped to Trust Services Criteria
#  10. HIPAA          — Controls mapped to HIPAA §164 safeguards
#  11. License Gaps   — Controls blocked by current license tier
#
# The XLSX is generated by calling an embedded Python script that uses openpyxl.
# This avoids requiring Excel or COM automation on the assessment machine.
#
# SECURITY: No tenant data is interpolated into Python code.
#           All data passes through JSON serialization → Python reads the JSON.
#

function Publish-NLSComplianceMatrix {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)] [hashtable] $Metadata,
        [Parameter(Mandatory)] [object[]]  $Findings,
        [Parameter(Mandatory)]
        [ValidateScript({
            if ($_ -match '\.\.[\\\/]') { throw 'Path traversal not allowed.' }
            return $true
        })]
        [string] $OutputPath
    )

    # Verify Python + openpyxl available
    $pythonCmd = $null
    foreach ($py in @('python3','python')) {
        try {
            $null = & $py -c 'import openpyxl' 2>&1
            if ($LASTEXITCODE -eq 0) { $pythonCmd = $py; break }
        } catch { }
    }
    if (-not $pythonCmd) {
        Write-Warning "openpyxl not available — skipping XLSX output. Install: pip install openpyxl"
        return
    }

    # Load control definitions for framework citations + license info
    $cdefs = @{}
    try { foreach ($c in (Get-NLSControlDefinitions)) { $cdefs[$c.ControlId] = $c } } catch { }

    # Build serializable data payload
    $payload = @{
        Metadata = @{
            TenantDomain   = [string]($Metadata.TenantDomain  ?? 'Unknown')
            AssessmentDate = [string]($Metadata.AssessmentDate ?? (Get-Date -Format 'yyyy-MM-dd'))
            ToolVersion    = [string]($Metadata.ToolVersion    ?? '4.5.5')
            Operator       = [string]($Metadata.Operator       ?? '')
        }
        Findings = @($Findings | ForEach-Object {
            $ctrl = $cdefs[$_.ControlId]
            @{
                ControlId       = [string]$_.ControlId
                Title           = [string]$_.Title
                Category        = [string]$_.Category
                State           = [string]$_.State
                Severity        = [string]$_.Severity
                Detail          = [string]$_.Detail
                CurrentValue    = [string]($_.CurrentValue  ?? '')
                RequiredValue   = [string]($_.RequiredValue ?? '')
                Remediation     = [string]($ctrl.Remediation ?? $_.Remediation ?? '')
                BusinessRisk    = [string]($ctrl.BusinessRisk ?? '')
                LicenseReq      = [string]($ctrl.LicenseRequirement ?? 'Included')
                FrameworkIds    = @($_.FrameworkIds ?? @() | ForEach-Object { [string]$_ })
                CIS             = [string]($ctrl.References.CIS   ?? '')
                SCuBA           = [string]($ctrl.References.SCuBA ?? '')
                NIST            = [string]($ctrl.References.NIST  ?? '')
                CMMC            = [string]($ctrl.References.CMMC  ?? '')
                ISO27001        = [string]($ctrl.References.ISO27001 ?? '')
                SOC2            = [string]($ctrl.References.SOC2    ?? '')
                HIPAA           = [string]($ctrl.References.HIPAA   ?? '')
                PCIDASSS        = [string]($ctrl.References.PCIDSS   ?? '')
                DISASTIG        = [string]($ctrl.References.DISASTIG ?? '')
                MITRE           = [string](($ctrl.References.MITRE ?? @()) -join ', ')
            }
        })
    }

    # Serialize JSON for the Python helper.
    # Audit fix (v4.6.x MED #4): the previous implementation wrote tenant
    # findings to $env:TEMP via [System.IO.Path]::GetTempFileName(), which
    # creates the file with default inherited permissions before any ACL
    # tightening can run. Two-step (create + chmod) is race-prone in a
    # shared TEMP. Instead, co-locate the helper JSON next to the XLSX
    # output (which already lives in the ACL-hardened output directory)
    # and harden it via Set-NLSSensitiveFileAcl as soon as we write it.
    $outputDir = Split-Path -Parent $OutputPath
    if (-not $outputDir -or -not (Test-Path -LiteralPath $outputDir)) {
        $outputDir = [System.IO.Path]::GetTempPath()
    }
    $matrixBase = [System.IO.Path]::GetFileNameWithoutExtension($OutputPath)
    if ([string]::IsNullOrWhiteSpace($matrixBase)) { $matrixBase = 'compliance-matrix' }
    $tmpJson = Join-Path $outputDir "$matrixBase-matrix-input.json"
    try {
        $payload | ConvertTo-Json -Depth 6 -Compress | Out-File -LiteralPath $tmpJson -Encoding utf8
        if (Get-Command Set-NLSSensitiveFileAcl -ErrorAction SilentlyContinue) {
            Set-NLSSensitiveFileAcl -Path $tmpJson -ErrorAction SilentlyContinue
        }

        # Embedded Python — reads JSON, writes XLSX, no string interpolation of tenant data
        $pyScript = @'
import json, sys, os
from openpyxl import Workbook
from openpyxl.styles import (Font, PatternFill, Alignment, Border, Side,
                              GradientFill)
from openpyxl.utils import get_column_letter

data_path = sys.argv[1]
out_path  = sys.argv[2]

with open(data_path, encoding='utf-8') as f:
    d = json.load(f)

meta     = d['Metadata']
findings = d['Findings']

# ── Color palette ─────────────────────────────────────────────────────────────
NAVY    = '0F2544'
ORANGE  = 'E8621A'
BLUE    = '3B7DD8'
WHITE   = 'FFFFFF'
LGRAY   = 'F4F7FB'
MGRAY   = 'E2E8F0'
DGRAY   = '6B7280'
RED     = 'DC2626'
AMBER   = 'CA8A04'
GREEN   = '059669'
ROSE    = 'FEF2F2'
AMBER_L = 'FFFBEB'
GREEN_L = 'F0FDF4'
NA_L    = 'F9FAFB'

SEV_COLORS = {'Critical':'DC2626','High':'EA580C','Medium':'CA8A04','Low':'65A30D','Informational':'6B7280'}
STATE_COLORS = {'Gap':ROSE,'Partial':AMBER_L,'Satisfied':GREEN_L,'NotApplicable':NA_L}
STATE_FONT   = {'Gap':'991B1B','Partial':'92400E','Satisfied':'166534','NotApplicable':'6B7280'}

def hdr(ws, row, col, val, bg=NAVY, fg=WHITE, bold=True, wrap=False, size=10):
    c = ws.cell(row=row, column=col, value=val)
    c.font = Font(name='Arial', bold=bold, color=fg, size=size)
    c.fill = PatternFill('solid', fgColor=bg)
    c.alignment = Alignment(horizontal='center', vertical='center', wrap_text=wrap)
    return c

def cell(ws, row, col, val, bg=None, fg='111827', bold=False, wrap=True, align='left'):
    c = ws.cell(row=row, column=col, value=val)
    c.font = Font(name='Arial', color=fg, bold=bold, size=9)
    if bg: c.fill = PatternFill('solid', fgColor=bg)
    c.alignment = Alignment(horizontal=align, vertical='top', wrap_text=wrap)
    return c

def thin_border(ws, row, col):
    t = Border(
        left=Side(style='thin',color='E2E8F0'),
        right=Side(style='thin',color='E2E8F0'),
        top=Side(style='thin',color='E2E8F0'),
        bottom=Side(style='thin',color='E2E8F0')
    )
    ws.cell(row=row, column=col).border = t

wb = Workbook()

# ═══════════════════════════════════════════════════════════════════════
# SHEET 1: SUMMARY
# ═══════════════════════════════════════════════════════════════════════
ws = wb.active
ws.title = 'Summary'
ws.sheet_view.showGridLines = False
ws.column_dimensions['A'].width = 28
ws.column_dimensions['B'].width = 18
ws.column_dimensions['C'].width = 22
ws.column_dimensions['D'].width = 18

# Title banner
ws.row_dimensions[1].height = 36
ws.merge_cells('A1:D1')
t = ws['A1']
t.value = f"M365 Security Assessment — {meta['TenantDomain']}"
t.font = Font(name='Arial', bold=True, size=16, color=WHITE)
t.fill = PatternFill('solid', fgColor=NAVY)
t.alignment = Alignment(horizontal='center', vertical='center')

ws.merge_cells('A2:D2')
s = ws['A2']
s.value = f"Generated: {meta['AssessmentDate']}  |  Tool: NLS-Assessment v{meta['ToolVersion']}"
s.font = Font(name='Arial', size=9, color='6B7280')
s.alignment = Alignment(horizontal='center')

# Score section
total = len(findings)
gaps  = sum(1 for f in findings if f['State']=='Gap')
parts = sum(1 for f in findings if f['State']=='Partial')
sats  = sum(1 for f in findings if f['State']=='Satisfied')
nas   = sum(1 for f in findings if f['State']=='NotApplicable')
scored = total - nas
score  = round(100*(sats+0.5*parts)/scored) if scored else 0
posture = 'Strong' if score>=85 else 'Moderate' if score>=65 else 'At Risk' if score>=40 else 'Critical Risk'

hdr(ws,4,1,'Metric',NAVY,WHITE); hdr(ws,4,2,'Value',NAVY,WHITE); hdr(ws,4,3,'Metric',NAVY,WHITE); hdr(ws,4,4,'Value',NAVY,WHITE)
rows4 = [
    ('Security Score', f'{score}/100', 'Posture', posture),
    ('Total Controls', total,          'Gaps',    gaps),
    ('Satisfied',      sats,           'Partial', parts),
    ('Not Applicable', nas,            'Assessed',scored),
]
for i,(l1,v1,l2,v2) in enumerate(rows4,5):
    cell(ws,i,1,l1,LGRAY,NAVY,True)
    cell(ws,i,2,v1,WHITE,'111827',True,align='center')
    cell(ws,i,3,l2,LGRAY,NAVY,True)
    cell(ws,i,4,v2,WHITE,'111827',True,align='center')
    ws.row_dimensions[i].height = 18

# Severity breakdown
ws.merge_cells('A10:D10')
hdr(ws,10,1,'Gap Severity Breakdown',ORANGE,WHITE)
sev_hdr = ['Critical','High','Medium','Low']
for j,s2 in enumerate(sev_hdr,1):
    hdr(ws,11,j,s2,SEV_COLORS[s2],WHITE,size=9)
    cnt = sum(1 for f in findings if f['State']=='Gap' and f['Severity']==s2)
    cell(ws,12,j,cnt,WHITE,SEV_COLORS[s2],True,align='center')

# Workload scores
ws.merge_cells('A14:D14')
hdr(ws,14,1,'Score by Workload',BLUE,WHITE)
hdr(ws,15,1,'Workload',MGRAY,NAVY); hdr(ws,15,2,'Score',MGRAY,NAVY); hdr(ws,15,3,'Gaps',MGRAY,NAVY); hdr(ws,15,4,'Controls',MGRAY,NAVY)
wl_names={'AAD':'Identity & Access','EXO':'Exchange Online','DNS':'DNS Email Auth','DEF':'Microsoft Defender',
          'SPO':'SharePoint/OneDrive','TMS':'Microsoft Teams','INT':'Intune/Endpoint','PVW':'Purview/Compliance','PPL':'Power Platform'}
from collections import defaultdict
wl_groups = defaultdict(list)
for f in findings:
    wl = f['ControlId'].split('-')[0]
    wl_groups[wl].append(f)
row=16
for wl in ['AAD','EXO','DEF','DNS','SPO','TMS','INT','PVW','PPL']:
    grp = wl_groups.get(wl,[])
    if not grp: continue
    wn  = sum(1 for f in grp if f['State']!='NotApplicable')
    ws2 = sum(1 for f in grp if f['State']=='Satisfied')
    wp2 = sum(1 for f in grp if f['State']=='Partial')
    wg2 = sum(1 for f in grp if f['State']=='Gap')
    wsc = round(100*(ws2+0.5*wp2)/wn) if wn else 0
    col2= GREEN if wsc>=85 else AMBER if wsc>=65 else 'EA580C' if wsc>=40 else RED
    cell(ws,row,1,wl_names.get(wl,wl),LGRAY,NAVY)
    cell(ws,row,2,f'{wsc}/100',WHITE,col2,True,align='center')
    cell(ws,row,3,wg2,WHITE,RED if wg2>0 else GREEN,True,align='center')
    cell(ws,row,4,len(grp),WHITE,'111827',align='center')
    ws.row_dimensions[row].height = 16
    row+=1

# Framework scores
fws = {'CIS':'CIS M365 v6.0.1','SCuBA':'CISA SCuBA','NIST':'NIST 800-53r5','CMMC':'CMMC 2.0 L2',
       'ISO27001':'ISO 27001:2022','SOC2':'SOC 2','HIPAA':'HIPAA §164','PCIDSS':'PCI DSS v4.0'}
ws.merge_cells(f'A{row+1}:D{row+1}')
hdr(ws,row+1,1,'Framework Compliance',NAVY,WHITE)
hdr(ws,row+2,1,'Framework',MGRAY,NAVY); hdr(ws,row+2,2,'Score',MGRAY,NAVY)
hdr(ws,row+2,3,'Mapped Controls',MGRAY,NAVY); hdr(ws,row+2,4,'Reference',MGRAY,NAVY)
row+=3
for fw, fname in fws.items():
    ff = [f for f in findings if any(fw in fid for fid in f['FrameworkIds'])]
    fd = [f for f in ff if f['State']!='NotApplicable']
    fs = sum(1 for f in fd if f['State']=='Satisfied')
    fp = sum(1 for f in fd if f['State']=='Partial')
    fsc= round(100*(fs+0.5*fp)/len(fd)) if fd else 0
    fcol=GREEN if fsc>=85 else AMBER if fsc>=65 else 'EA580C' if fsc>=40 else RED
    cell(ws,row,1,fname,LGRAY,NAVY); cell(ws,row,2,f'{fsc}%',WHITE,fcol,True,align='center')
    cell(ws,row,3,len(fd),WHITE,'111827',align='center'); cell(ws,row,4,fw,WHITE,DGRAY)
    ws.row_dimensions[row].height=16; row+=1

