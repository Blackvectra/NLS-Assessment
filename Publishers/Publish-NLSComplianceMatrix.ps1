def build_findings_sheet(wb, title, rows, columns):
    ws2 = wb.create_sheet(title)
    ws2.sheet_view.showGridLines = False
    ws2.freeze_panes = 'A3'
    # Banner
    ws2.merge_cells(f'A1:{get_column_letter(len(columns))}1')
    t2 = ws2['A1']
    t2.value = f'{title} — {meta["TenantDomain"]}'
    t2.font = Font(name='Arial', bold=True, size=11, color=WHITE)
    t2.fill = PatternFill('solid', fgColor=NAVY)
    t2.alignment = Alignment(horizontal='center', vertical='center')
    ws2.row_dimensions[1].height = 26
    # Headers
    for j,(col_name,width) in enumerate(columns,1):
        hdr(ws2, 2, j, col_name, ORANGE, WHITE, size=9)
        ws2.column_dimensions[get_column_letter(j)].width = width
    ws2.row_dimensions[2].height = 18
    # Data
    for i,row2 in enumerate(rows,3):
        bg = STATE_COLORS.get(row2.get('State',''),'')
        fg_state = STATE_FONT.get(row2.get('State',''),'111827')
        ws2.row_dimensions[i].height = max(15, min(60, 10 + len(str(row2.get('Detail','')))/8))
        for j,(col_name,_) in enumerate(columns,1):
            key = col_name.replace(' ','').replace('/','')
            val = row2.get(col_name, row2.get(key, ''))
            fg2 = fg_state if col_name == 'State' else ('111827' if col_name not in ('Severity',) else SEV_COLORS.get(str(val),'111827'))
            c2 = cell(ws2, i, j, val, bg, fg2, bold=(col_name=='State'))
            thin_border(ws2, i, j)
    # Auto-filter
    ws2.auto_filter.ref = f'A2:{get_column_letter(len(columns))}{len(rows)+2}'
    return ws2

ALL_COLS = [
    ('Control ID',18),('Title',36),('Category',14),('State',12),
    ('Severity',10),('Current Value',22),('Required Value',22),
    ('Business Risk',36),('Remediation',40),('CIS',12),('SCuBA',16),
    ('NIST',14),('CMMC',14),('ISO 27001',14),('SOC 2',14),('HIPAA',14),
    ('License',28),('MITRE',20),
]

def map_row(f):
    return {
        'Control ID': f['ControlId'], 'Title': f['Title'], 'Category': f['Category'],
        'State': f['State'], 'Severity': f['Severity'],
        'Current Value': f['CurrentValue'], 'Required Value': f['RequiredValue'],
        'Business Risk': f['BusinessRisk'], 'Remediation': f['Remediation'],
        'CIS': f['CIS'], 'SCuBA': f['SCuBA'], 'NIST': f['NIST'], 'CMMC': f['CMMC'],
        'ISO 27001': f.get('ISO27001',''), 'SOC 2': f.get('SOC2',''),
        'HIPAA': f.get('HIPAA',''), 'License': f['LicenseReq'], 'MITRE': f['MITRE'],
        'Detail': f['Detail'],
    }

all_rows  = [map_row(f) for f in sorted(findings, key=lambda x:(
    ['Gap','Partial','Satisfied','NotApplicable'].index(x['State']) if x['State'] in
    ['Gap','Partial','Satisfied','NotApplicable'] else 9, x['ControlId']))]
gap_rows  = [r for r in all_rows if r['State'] in ('Gap','Partial')]

# Sheet 2: All Controls
build_findings_sheet(wb, 'All Controls', all_rows, ALL_COLS)

# Sheet 3: Gaps Only
build_findings_sheet(wb, 'Gaps & Partials', gap_rows, ALL_COLS)

# Framework-specific sheets
FW_SHEETS = [
    ('CIS M365 v6', 'CIS',      'CIS',       [('Control ID',16),('Title',36),('State',12),('Severity',10),('CIS Ref',14),('Remediation',40)]),
    ('CISA SCuBA',  'SCuBA',    'SCuBA',      [('Control ID',16),('Title',36),('State',12),('Severity',10),('SCuBA Ref',18),('Remediation',40)]),
    ('NIST 800-53', 'NIST',     'NIST',       [('Control ID',16),('Title',36),('State',12),('Severity',10),('NIST Ref',14),('Business Risk',40)]),
    ('CMMC 2.0',    'CMMC',     'CMMC',       [('Control ID',16),('Title',36),('State',12),('Severity',10),('CMMC Ref',14),('Remediation',40)]),
    ('ISO 27001',   'ISO27001', 'ISO 27001',  [('Control ID',16),('Title',36),('State',12),('Severity',10),('ISO Ref',14),('Remediation',40)]),
    ('SOC 2',       'SOC2',     'SOC 2',      [('Control ID',16),('Title',36),('State',12),('Severity',10),('SOC2 Ref',14),('Remediation',40)]),
    ('HIPAA §164',  'HIPAA',    'HIPAA',      [('Control ID',16),('Title',36),('State',12),('Severity',10),('HIPAA Ref',14),('Remediation',40)]),
]
for sheet_title, fw_key, ref_col_name, cols in FW_SHEETS:
    fw_rows = []
    for f in findings:
        ref = f.get(fw_key,'') or (f.get('ISO27001','') if fw_key=='ISO27001' else '') or \
              (f.get('SOC2','') if fw_key=='SOC2' else '') or \
              (f.get('HIPAA','') if fw_key=='HIPAA' else '')
        if not ref: continue
        fw_rows.append({
            'Control ID': f['ControlId'], 'Title': f['Title'],
            'State': f['State'], 'Severity': f['Severity'],
            f'{ref_col_name} Ref': ref, 'Remediation': f['Remediation'],
            'Business Risk': f['BusinessRisk'], 'Detail': f['Detail'],
        })
    fw_rows.sort(key=lambda x:['Gap','Partial','Satisfied','NotApplicable'].index(x['State'])
                 if x['State'] in ['Gap','Partial','Satisfied','NotApplicable'] else 9)
    if fw_rows:
        build_findings_sheet(wb, sheet_title, fw_rows, cols)

# License gaps sheet
lic_rows = [map_row(f) for f in findings if f['State']=='Gap' and 'Included' not in f['LicenseReq']]
if lic_rows:
    build_findings_sheet(wb, 'License Gaps', lic_rows,
        [('Control ID',16),('Title',36),('State',12),('Severity',10),('License',32),('Remediation',40)])

wb.save(out_path)
print(f"XLSX saved: {out_path}")
